package com.dc.kafka.component;

public class TConfigDomain {
	    private int cfgId;

		private String cfgKey;

	    private String cfgValue;

	    private String cfgExtra;
	    
	    private int cfgType;	

	    private int cfgStatus;

		public int getCfgId() {
			return cfgId;
		}

		public void setCfgId(int cfgId) {
			this.cfgId = cfgId;
		}

		public String getCfgKey() {
			return cfgKey;
		}

		public void setCfgKey(String cfgKey) {
			this.cfgKey = cfgKey;
		}

		public String getCfgValue() {
			return cfgValue;
		}

		public void setCfgValue(String cfgValue) {
			this.cfgValue = cfgValue;
		}

		public String getCfgExtra() {
			return cfgExtra;
		}

		public void setCfgExtra(String cfgExtra) {
			this.cfgExtra = cfgExtra;
		}

		public int getCfgType() {
			return cfgType;
		}

		public void setCfgType(int cfgType) {
			this.cfgType = cfgType;
		}

		public int getCfgStatus() {
			return cfgStatus;
		}

		public void setCfgStatus(int cfgStatus) {
			this.cfgStatus = cfgStatus;
		}
	    
	    
}
